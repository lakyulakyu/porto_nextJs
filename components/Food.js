import Link from "next/link";
import React, { useState } from "react";
import { data, kategori } from "../data/data";

const Food = () => {
  const [foods, setFoods] = useState(data);

  const filterType = (category) => {
    setFoods(
      data.filter((item) => {
        return item.category === category;
      })
    );
  };
  return (
    <div className="max-w-[1640px] m-auto px-4 py-12">
      <h1 className="text-orange-600 font-bold text-4xl text-center mb-12 md:mb-0">
        Menu Teratas Minggu Ini
      </h1>
      <div className="flex flex-col lg:flex-row justify-between">
        <div>
          <p className="font-bold text-gray-700">Filter Type</p>
          <div className="flex justfiy-between flex-wrap">
            <button
              onClick={() => setFoods(data)}
              className="m-1 border-orange-600 text-orange-600 hover:bg-orange-600 hover:text-white"
            >
              All
            </button>
            {kategori.map((isi) => (
            <button
              onClick={() => filterType(isi.categories)}
              className="m-1 border-orange-600 text-orange-600 hover:bg-orange-600 hover:text-white"
            >
              {isi.name}
            </button>
            ))}
          </div>
        </div>
      </div>

      {/* Display foods */}
      <div className="grid grid-cols-2 lg:grid-cols-4 gap-6 pt-4">
        {foods.map((item, index) => (
            <Link
              key={index}
              href={`/${item.id}`}
              className="border-0 shadow-lg rounded-lg hover:scale-105 duration-300 p-0"
            >
              <img
                src={item.image}
                alt={item.name}
                className="w-full h-[200px] object-cover rounded-t-lg"
              />
              <div className="flex text-slate-600 justify-between px-2 py-4">
                <p className="font-bold">{item.name}</p>
                <p>
                  <span className=" text-green-800 p-1 rounded-full">
                    {item.price}
                  </span>
                </p>
              </div>
              
            </Link>
            
        ))}
        
      </div>
    </div>
  );
};

export default Food;
